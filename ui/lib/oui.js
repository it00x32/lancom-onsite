/**
 * OUI-Hinweis aus der MAC (IEEE: erste 3 Oktette).
 * Kompakte Teilmenge — Vollständigkeit: IEEE MA-L/MA-M/OUI-Datenbank.
 */

export function macToOuiKey(mac) {
  if (!mac || typeof mac !== 'string') return null;
  const p = mac.trim().replace(/[^0-9A-Fa-f]/g, '');
  if (p.length < 6) return null;
  return p.slice(0, 6).toUpperCase();
}

export function formatOui(key6) {
  if (!key6 || key6.length !== 6) return null;
  return `${key6.slice(0, 2)}:${key6.slice(2, 4)}:${key6.slice(4, 6)}`;
}

/** Bit 2 im ersten Oktett: lokal verwaltet (häufig Privacy-/Random-MAC) */
export function isLocallyAdministeredMac(mac) {
  const key = macToOuiKey(mac);
  if (!key) return false;
  const first = parseInt(key.slice(0, 2), 16);
  return (first & 0x02) !== 0;
}

/**
 * @param {string} mac
 * @returns {{ oui: string|null, vendor: string|null, local: boolean }}
 */
export function lookupMacVendor(mac) {
  const key = macToOuiKey(mac);
  if (!key) return { oui: null, vendor: null, local: false };
  const oui = formatOui(key);
  const local = isLocallyAdministeredMac(mac);
  if (local) return { oui, vendor: null, local: true };
  return { oui, vendor: OUI_MAP[key] || null, local: false };
}

